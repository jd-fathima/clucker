@login_required
def feed(request):
    form = PostForm()
    current_user = request.user
    authors = list(current_user.followees.all()) + [current_user]
    posts = Post.objects.filter(author__in=authors)

    return render(request, 'feed.html', {'form': form, 'user':current_user, 'posts': posts})

